#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int c;
	int s;

	i = 0;
	c = 0;
	s = 1;
	while (str[i] == '\n' || str[i] == '\t' || str[i] == '\f'
			|| str[i] == ' ' || str[i] == '\r' || str[i] == '\v')
		i++;
	if (str[i] == '-')
	{
		s = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		c = (c * 10) + (str[i] - '0');
		i++;
	}
	return (c * s);
}
